@ID_FACTURA INT=NULL,
@ID_CLIENTE INT=NULL,
@ID_CIUDAD INT=NULL
AS
BEGIN
	BEGIN TRY
	MERGE INTO BACKUP_VNT_FACTURACIONES T2  USING VNT_FACTURACIONES T1
	ON T1.VNT_ID_FACTURA=T2.VNT_ID_FACTURA
	WHEN NOT MATCHED THEN
	INSERT
	(
		VNT_ID_FACTURA ,
		CTL_CVE_FKCLIENTE ,
		VNT_CVE_FKCIUDAD ,
		CBR_ESTADO_FACTURA ,
