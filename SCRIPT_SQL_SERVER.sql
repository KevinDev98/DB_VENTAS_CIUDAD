--DROP PROCEDURE SP_DELETE_FACUTRAS_BY_ID
CREATE   PROCEDURE [dbo].[SP_DELETE_FACUTRAS_BY_ID]
@ID_FACTURA INT=NULL,
@ID_CLIENTE INT=NULL,
@ID_CIUDAD INT=NULL
AS
BEGIN
	BEGIN TRY
	MERGE INTO BACKUP_VNT_FACTURACIONES T2  USING VNT_FACTURACIONES T1
	ON T1.VNT_ID_FACTURA=T2.VNT_ID_FACTURA
	WHEN NOT MATCHED THEN
	INSERT
	(
		VNT_ID_FACTURA ,
		CTL_CVE_FKCLIENTE ,
		VNT_CVE_FKCIUDAD ,
		CBR_ESTADO_FACTURA ,
		VNT_MONTO_FACTURA ,
		VNT_IVA_FACTURA ,
		VNT_FECHA_FACTURA 
	)
	VALUES
	(
		VNT_ID_FACTURA ,
		CTL_CVE_FKCLIENTE ,
		VNT_CVE_FKCIUDAD ,
		CBR_ESTADO_FACTURA ,
		VNT_MONTO_FACTURA ,
		VNT_IVA_FACTURA ,
		VNT_FECHA_FACTURA 
	)
	OUTPUT $action, 
	INSERTED.VNT_ID_FACTURA ,
	INSERTED.CTL_CVE_FKCLIENTE ,
	INSERTED.VNT_CVE_FKCIUDAD ,
	INSERTED.CBR_ESTADO_FACTURA ,
	INSERTED.VNT_MONTO_FACTURA ,
	INSERTED.VNT_IVA_FACTURA ,
	INSERTED.VNT_FECHA_FACTURA ; 
	SELECT @@ROWCOUNT;
	
	IF @ID_CLIENTE IS NOT NULL 
	BEGIN
		DELETE FROM VNT_FACTURACIONES WHERE  CTL_CVE_FKCLIENTE=@ID_CLIENTE
	END
	IF @ID_FACTURA IS NOT NULL
	BEGIN
		DELETE FROM VNT_FACTURACIONES WHERE  VNT_ID_FACTURA=@ID_FACTURA
	END
	IF @ID_CIUDAD IS NOT NULL
	BEGIN
		DELETE FROM VNT_FACTURACIONES WHERE  VNT_CVE_FKCIUDAD=@ID_CIUDAD
	END
	PRINT('FACTURAS ELIMINADAS CORRECTAMENTE')

	END TRY
	BEGIN CATCH
		PRINT('ERROR ELIMINANDO LA FACTURA')
	END CATCH
END
GO

